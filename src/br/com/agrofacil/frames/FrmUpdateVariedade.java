    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed
       int result = JOptionPane.showConfirmDialog(this, "Você Tem Certeza ??","Atualizar Variedade",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
        if (result==2) return;

        try {

            variedadeInEdit.setDescricao(txtDescricao.getText());
            variedadeInEdit.setMesIniColheita(Integer.parseInt(cbMesIni.getSelectedItem().toString()));
            variedadeInEdit.setMesFinColheita(Integer.parseInt(cbMesFin.getSelectedItem().toString()));
            
            if (cbMaturada.getSelectedIndex() == 0) {
                variedadeInEdit.setMaturada(true);
            } else {
                variedadeInEdit.setMaturada(false);
            }
            
            dao.updateVariedade(variedadeInEdit);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro Atualizando Variedade "+e,"Error",JOptionPane.ERROR_MESSAGE);
        }
        try {
            control.loadInitialData();
        } catch (SQLException ex) {
            Logger.getLogger(FrmUpdateVariedade.class.getName()).log(Level.SEVERE, null, ex);
        }
        setVisible(false);
    }//GEN-LAST:event_btOkActionPerformed

