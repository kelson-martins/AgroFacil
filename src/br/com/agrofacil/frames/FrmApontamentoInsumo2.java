   public void loadInitialData() throws SQLException {
       final List<String> columns = new ArrayList<String>();
       final List<String[]> values = new ArrayList<String[]>();      
       
       columns.add("Sequencia");
       columns.add("Dose");
       columns.add("Equipamento");
       columns.add("Vazao");
       columns.add("QtdTotal");
       
    try {
            apontamento = dao.getAllApontamentoInsumo();
            tblApontamento.setModel(new MyTableModel(ApontamentoInsumo.class, apontamento, tblApontamento));
            tblItens.setModel(new MyTableModel(ApontamentoInsumoItem.class,new LinkedList<ApontamentoInsumoItem>() , tblItens));    
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        
    }  
 

       private void addSellTableListener() {
       
       final List<String> columns = new ArrayList<String>();
       final List<String[]> values = new ArrayList<String[]>();      
       
       columns.add("Sequencia");
       columns.add("Dose");
       columns.add("Equipamento");
       columns.add("Vazao");
       columns.add("QtdTotal");
       
       
        tblApontamento.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            
            public void valueChanged(ListSelectionEvent e) {
                try {
                    itens = dao.getApontamentoInsumoItem(apontamento.get(tblApontamento.getSelectedRow()));
                } catch (SQLException ex) {
                    Logger.getLogger(FrmApontamentoInsumo2.class.getName()).log(Level.SEVERE, null, ex);
                }
                if (itens.size() > 0) {
                        values.clear();
                        for (int i = 0; i < itens.size(); i++) {
                            values.add(new String[] {String.valueOf(itens.get(i).getSequencia()),
                                    String.valueOf(itens.get(i).getDoseProgramada()),
                                    itens.get(i).getEquipamento().toString(),
                                    String.valueOf(itens.get(i).getVazaoProgramada()),
                                    String.valueOf(itens.get(i).getQtdTotal()
                                    )});
                        }   
                        TableModel tableModel = new DefaultTableModel(values.toArray(new Object[][] {}), columns.toArray());
                        tblItens.setModel(tableModel); 
                        tblItens.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
                        TableColumnAdjuster tca = new TableColumnAdjuster(tblItens);
                        tca.setDynamicAdjustment(true);
                        tca.adjustColumns();
                    } 
            }
        });
    }

       
