    private void btSelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSelecionarActionPerformed
        
        if (jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um Registro !!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            List<OrdemCorteMuda> lista = new Vector<OrdemCorteMuda>();
            List<Fazenda> listaFazenda = new Vector<Fazenda>();
            List<Safra> listaSafra = new Vector<Safra>();
            List<Setor> listaSetor = new Vector<Setor>();
            List<Talhao> listaTalhao = new Vector<Talhao>();
            
            lista.add(ordem.get(jTable1.getSelectedRow()));
            listaFazenda.add(lista.get(0).getFazenda());
            listaSafra.add(lista.get(0).getSafra());
            listaSetor.add(lista.get(0).getSetor());
            listaTalhao.add(lista.get(0).getTalhao());
            
            DefaultComboBoxModel dcbmCentro = new DefaultComboBoxModel(new Vector<OrdemCorteMuda>(lista));                
            DefaultComboBoxModel dcbmSafra = new DefaultComboBoxModel(new Vector<Safra>(listaSafra));    
            DefaultComboBoxModel dcbmFazenda = new DefaultComboBoxModel(new Vector<Fazenda>(listaFazenda));    
            DefaultComboBoxModel dcbmSetor = new DefaultComboBoxModel(new Vector<Setor>(listaSetor));    
            DefaultComboBoxModel dcbmTalhao = new DefaultComboBoxModel(new Vector<Talhao>(listaTalhao));    
            
            comboOrdem.setModel(dcbmCentro);
            comboSafra.setModel(dcbmSafra);
            comboFazenda.setModel(dcbmFazenda);
            comboSetor.setModel(dcbmSetor);
            comboTalhao.setModel(dcbmTalhao);            
            
            this.setVisible(false);
        }
    }//GEN-LAST:event_btSelecionarActionPerformed

    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        List<String> columns = new ArrayList<String>();
        List<String[]> values = new ArrayList<String[]>();
        columns.add("ID");
        columns.add("Safra");
        columns.add("Fazenda");
        columns.add("Setor");
        columns.add("TalhÃ£o");
        columns.add("Data");
        columns.add("Quantidade");
        
        try {
            ordem = dao.getOrdemCorteMudaByName(tfPesquisa.getText());            
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        if (ordem.size() > 0) {
            for (int i = 0; i < ordem.size(); i++) {
                values.add(new String[] {String.valueOf(ordem.get(i).getId()),String.valueOf(ordem.get(i).getSafra().getDataInicioMoagem()),
                    ordem.get(i).getFazenda().getDescricao(),ordem.get(i).getSetor().getDescricao(),ordem.get(i).getTalhao().getDescricao(),
                String.valueOf(ordem.get(i).getDataOrdem()),
                String.valueOf(ordem.get(i).getQtdAreaTotal())});
            }               
        }                        
       TableModel tableModel = new DefaultTableModel(values.toArray(new Object[][] {}), columns.toArray());
       jTable1.setModel(tableModel); 
       jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        TableColumnAdjuster tca = new TableColumnAdjuster(jTable1);
        tca.setDynamicAdjustment(true);
        
        tca.adjustColumns();       
    }//GEN-LAST:event_btPesquisarActionPerformed

