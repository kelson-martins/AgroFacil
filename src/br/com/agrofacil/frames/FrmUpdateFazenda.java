    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed
        try {

            fazendaInEdit.setDescricao(txtDescricao.getText());
            fazendaInEdit.setMunicipio((Municipio)cbMunicipio.getSelectedItem());
            fazendaInEdit.setEndereco(txtEndereco.getText());
            fazendaInEdit.setTelefone(txtTelefone.getText());
            fazendaInEdit.setCpfcgc(txtCpfCnpj.getText());           
            fazendaInEdit.setInscricaoIncra(txtInscIncra.getText());
            fazendaInEdit.setInscricaoProdutor(txtInscProd.getText());
            fazendaInEdit.setCep(txtCep.getText());
            fazendaInEdit.setTipoPessoa(TipoPessoa.valueOf(cbTipoPessoa.getSelectedItem().toString()));
            if (cbFUnrual.getSelectedIndex() == 0) {
                fazendaInEdit.setFunrual(true);
            } else {
                fazendaInEdit.setFunrual(false);
            }
            fazendaInEdit.setAreaTotal(Float.parseFloat(txtAreaTot.getText()));
            fazendaInEdit.setAreaArrendamento(Float.parseFloat(txtAreaArrend.getText()));
            fazendaInEdit.setAreaProdutiva(Float.parseFloat(txtAreaProd.getText()));
            fazendaInEdit.setRazaoSocialFundo(txtRazao.getText());
            fazendaInEdit.setImovelReceita(txtImovel.getText());
            fazendaInEdit.setQtdAreaMat(Float.parseFloat(txtAreaMat.getText()));
            fazendaInEdit.setCnpjPesFis(txtPesFis.getText());
            
            dao.updateFazenda(fazendaInEdit);

            int result = JOptionPane.showConfirmDialog(this, "Você Tem Certeza ??","Atualizar Fazenda",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(result != 0) {
                return;
            }
            control.loadInitialData();
            setVisible(false);
        } catch(Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao Adicionar Fazenda "+e,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btOkActionPerformed

