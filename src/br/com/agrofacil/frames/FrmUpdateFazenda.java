private void initMyComponents() throws SQLException {  
   
    this.btOk.setIcon(new ImageIcon("icons/check16.png"));
    this.btCancel.setIcon(new ImageIcon("icons/cancel16.png"));
   
   List<Municipio> municipio = new MunicipioDAO().getAllMunicipio();
   DefaultComboBoxModel dcbm = new DefaultComboBoxModel(new Vector<Municipio>(municipio));
   cbMunicipio.setModel(dcbm);
   
   
   txtDescricao.setText(fazendaInEdit.getDescricao());
   cbMunicipio.setSelectedItem(fazendaInEdit.getMunicipio());
   txtEndereco.setText(fazendaInEdit.getEndereco());
   txtCpfCnpj.setText(fazendaInEdit.getCpfcgc());
   txtInscIncra.setText(fazendaInEdit.getInscricaoIncra());
   txtInscProd.setText(fazendaInEdit.getInscricaoProdutor());
   txtCep.setText(fazendaInEdit.getCep());
   cbTipoPessoa.setSelectedItem(TipoPessoa.valueOf(fazendaInEdit.getTipoPessoa().toString()));
   if (fazendaInEdit.isFunrual()== true) {
       cbFUnrual.setSelectedIndex(0);
   } else {
       cbFUnrual.setSelectedIndex(1);
   }
   txtAreaTot.setText(String.valueOf(fazendaInEdit.getAreaTotal()));
   txtAreaArrend.setText(String.valueOf(fazendaInEdit.getAreaArrendamento()));
   txtAreaProd.setText(String.valueOf(fazendaInEdit.getAreaProdutiva()));
   txtRazao.setText(fazendaInEdit.getRazaoSocialFundo());
   txtImovel.setText(fazendaInEdit.getImovelReceita());
   txtAreaMat.setText(String.valueOf(fazendaInEdit.getQtdAreaMat()));
   txtPesFis.setText(fazendaInEdit.getCnpjPesFis());
}    
    
