    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed
        try {
            Espacamento espacamento = new Espacamento();

            espacamento.setDescricao(txtDescricao.getText());
            espacamento.setMetrosEspacamento(Float.parseFloat(txtMetros.getText().replace(",", ".")));
            espacamento.setQntRuasCana(Float.parseFloat(txtQntCana.getText().replace(",", ".")));

            dao.addEspacamento(espacamento);

            int result = JOptionPane.showConfirmDialog(this, "Você Tem Certeza ??","Adicionar Espaçamento",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(result != 0) {
                return;
            }
            control.loadInitialData();
            setVisible(false);
        } catch(Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao Adicionar Espaçamento "+e,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btOkActionPerformed

