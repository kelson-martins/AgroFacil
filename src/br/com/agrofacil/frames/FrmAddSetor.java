    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed
        try {
            Setor setor = new Setor(0,0,0);

            setor.setDescricao(txtDescricao.getText());
            setor.setFazenda((Fazenda)cbFazenda.getSelectedItem());
            setor.setCep(txtCep.getText());
            setor.setAreaTotal(Float.parseFloat(txtAreaTot.getText()));
            setor.setAreaArrendamento(Float.parseFloat(txtAreaArrend.getText()));
            setor.setAreaProdutiva(Float.parseFloat(txtAreaProd.getText()));

            int result = JOptionPane.showConfirmDialog(this, "Você Tem Certeza ??","Adicionar Setor",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
            
            if(result != 0) {
                return;
            }
            
            dao.addSetor(setor);

            control.loadInitialData();
            setVisible(false);
        } catch(Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao Adicionar Setor "+e,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btOkActionPerformed

