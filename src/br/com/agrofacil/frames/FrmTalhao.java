    private void btRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRemoverActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Escolha Talhao para Remover !!");
            return;
        }

        int result = JOptionPane.showConfirmDialog(this, "Você tem Certeza ??","Remover Talhao",JOptionPane.OK_CANCEL_OPTION);

        if(result != 0) {
            return;
        }
        try {
            try {
                dao.deleteTalhao(
                        talhao.get(jTable1.getSelectedRow()).getId());
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Erro ao Remover Talhao","Remover Talhao",JOptionPane.ERROR_MESSAGE);
            }
            loadInitialData();
        } catch (SQLException ex) {
            Logger.getLogger(FrmTalhao.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btRemoverActionPerformed

