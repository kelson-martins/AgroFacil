   public void loadInitialData() throws SQLException {
        
        List<String> columns = new ArrayList<String>();
        List<String[]> values = new ArrayList<String[]>();
        columns.add("Id");
        columns.add("Descrição");
        columns.add("Fazenda");
        columns.add("Setor");
        columns.add("Safra");
        columns.add("Frente Transporte");
        columns.add("Distancia Terra");
        columns.add("Distancia Asfalto");
        columns.add("Distancia Hidrovia");
        columns.add("Variedade");
        columns.add("Espacamento");
        columns.add("Qtd Area Prod");
        columns.add("Qtd Area Muda");
        columns.add("Empresa");
        columns.add("Unidade Industrial");
        columns.add("Data Situacao");
        columns.add("Situacao");
        
        talhao = dao.getAllTalhao();
        if (talhao.size() > 0) {
            for (int i = 0; i < talhao.size(); i++) {
                values.add(new String[] {String.valueOf(talhao.get(i).getId()), 
                talhao.get(i).getDescricao(),
                talhao.get(i).getFazenda().getDescricao(),
                talhao.get(i).getSetor().getDescricao(), 
                String.valueOf(talhao.get(i).getSafra().getId()),
                talhao.get(i).getFrenteTransporte().getDescricao(),
                String.valueOf(talhao.get(i).getDistTerra()),
                String.valueOf(talhao.get(i).getDistAsfalto()),
                String.valueOf(talhao.get(i).getDistHidrovia()),
                talhao.get(i).getVariedade().getDescricao(),
                talhao.get(i).getEspacamento().getDescricao(),
                String.valueOf(talhao.get(i).getQtdAreaProd()), 
                String.valueOf(talhao.get(i).getQtdAreaMuda()), 
                talhao.get(i).getEmpresa().getDescricao(),
                talhao.get(i).getUnidadeIndustrial().getDescricao(),
                String.valueOf(talhao.get(i).getDataSituacao()),                
                
                String.valueOf(talhao.get(i).getSituacao())});
            }               
        }                        
       TableModel tableModel = new DefaultTableModel(values.toArray(new Object[][] {}), columns.toArray());
       jTable1.setModel(tableModel); 
      
       
       //for (int i = 0; i < columns.size(); i++) {
         // jTable1.getColumnModel().getColumn(i).setPreferredWidth(200); 
       //}
       
       jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        TableColumnAdjuster tca = new TableColumnAdjuster(jTable1);
        //tca.setColumnDataIncluded(true);
        //tca.setOnlyAdjustLarger(true);
        tca.setDynamicAdjustment(true);
        
        tca.adjustColumns();
        //tca.adjustColumn(1);
 
    }
    
