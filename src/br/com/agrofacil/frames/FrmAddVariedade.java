    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed
        try {
            Variedade variedade = new Variedade();

            variedade.setDescricao(txtDescricao.getText());
            variedade.setMesIniColheita(Integer.parseInt(cbMesIni.getSelectedItem().toString()));
            variedade.setMesFinColheita(Integer.parseInt(cbMesFin.getSelectedItem().toString()));
            
            if (cbMaturada.getSelectedIndex() == 0) {
                variedade.setMaturada(true);
            } else {
                variedade.setMaturada(false);
            }
                        
            dao.addVariedade(variedade);

            int result = JOptionPane.showConfirmDialog(this, "Você Tem Certeza ??","Adicionar Variedade",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(result != 0) {
                return;
            }
            control.loadInitialData();
            setVisible(false);
        } catch(Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao Adicionar Variedade "+e,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btOkActionPerformed

