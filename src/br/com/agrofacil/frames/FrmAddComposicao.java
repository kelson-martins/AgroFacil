    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed
        try {
            Composicao composicao = new Composicao();

            composicao.setDescricao(txtDescricao.getText());
            composicao.setVolumesTotais(Integer.parseInt(txtVolumes.getText()));

            dao.addComposicao(composicao);

            int result = JOptionPane.showConfirmDialog(this, "Você Tem Certeza ??","Adicionar Composição",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
            if(result != 0) {
                return;
            }
            control.loadInitialData();
            setVisible(false);
        } catch(Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao Adicionar Composição "+e,"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btOkActionPerformed

