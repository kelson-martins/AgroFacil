    private void btOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btOkActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Você Tem Certeza ??","Atualizar Unidad de Medida",JOptionPane.OK_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE);
        if (result==2) return;

        userInEdit.setDescricaoUnidade(txtDescricao.getText());
        try {
            dao.updateUnidadeMedida(userInEdit);
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error adding user "+ex,"Error",JOptionPane.ERROR_MESSAGE);
        }
        control.loadInitialData();
        setVisible(false);
    }//GEN-LAST:event_btOkActionPerformed

